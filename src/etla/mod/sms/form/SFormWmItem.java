    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbWmItem) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        moTextCode.setText(moRegistry.getCode());
        moTextName.setText(moRegistry.getName());
        moTextCodeRev.setText(moRegistry.getProductId());
        
       
        setFormEditable(true);
        

        addAllListeners();
    }

    @Override
    public SDbWmItem getRegistry() throws Exception {
        SDbWmItem registry = moRegistry.clone();

        if (registry.isRegistryNew()) {}

        registry.setCode(moTextCode.getValue());
        registry.setName(moTextName.getValue());
        registry.setProductId(moTextCodeRev.getValue());
        
        return registry;
    }

